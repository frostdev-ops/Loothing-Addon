--- Table mapping item IDs to base item levels (normal difficulty)
--- @type table<number, number>
--- Note: From Sepulcher of the First Ones onward, tokens report their actual item level
--- This table is maintained for historical tokens but may be empty for modern content
_G.LoothingTokenIlvls = {
	-- Empty for now - modern tokens report their own item level
	-- Historical data can be added here if needed for legacy content
}

--- Check if an item is a tier token
--- @param itemID number The item ID to check
--- @return boolean isToken True if the item is a token, false otherwise
function TokenData:IsToken(itemID)
	return LoothingTokenTable[itemID] ~= nil
end

--- Get the equipment slot for a token
--- @param itemID number The item ID to check
--- @return string|nil slot The equipment slot name (e.g., "HeadSlot", "ChestSlot"), or nil if not a token
function TokenData:GetSlot(itemID)
	return LoothingTokenTable[itemID]
end

--- Get the base item level for a token (normal difficulty)
--- @param itemID number The item ID to check
--- @return number|nil ilvl The base item level, or nil if not found
--- Note: Modern tokens (Sepulcher+) report their actual item level and may not be in this table
function TokenData:GetIlvl(itemID)
	return LoothingTokenIlvls[itemID]
end

--- Get the classes that can use a token
--- This function queries the game API to determine which classes can use the token
--- @param itemLink string The item link (e.g., from C_Item.GetItemInfo)
--- @return number classFlags Bitmask of classes that can use this token (same as C_Item.GetItemClassInfo)
function TokenData:GetClasses(itemLink)
	if not itemLink then
		return 0xFFFFFFFF -- All classes if no link provided
	end

	-- Use the tooltip API to determine usability
	-- This leverages the game's built-in logic for token class restrictions
	local tooltipData = C_TooltipInfo.GetHyperlink(itemLink)
	if not tooltipData or not tooltipData.lines then
		return 0xFFFFFFFF
	end

	-- Parse tooltip for "Classes: ..." line
	-- The actual implementation would need to parse class names and convert to bitmask
	-- For now, return a default value
	-- TODO: Implement full class parsing from tooltip
	return 0xFFFFFFFF
end

--- Get a human-readable slot name from a slot constant
--- @param slot string The slot constant (e.g., "HeadSlot", "ChestSlot")
--- @return string slotName The human-readable slot name (e.g., "Head", "Chest")
function TokenData:GetSlotName(slot)
	local slotNames = {
		["HeadSlot"] = "Head",
		["ShoulderSlot"] = "Shoulder",
		["BackSlot"] = "Back",
		["ChestSlot"] = "Chest",
		["HandsSlot"] = "Hands",
		["LegsSlot"] = "Legs",
		["WristSlot"] = "Wrist",
		["WaistSlot"] = "Waist",
		["FeetSlot"] = "Feet",
		["Trinket"] = "Trinket",
		["MultiSlots"] = "Multi-Slot",
	}

	return slotNames[slot] or slot
end

--- Get all tokens for a specific slot
--- @param slot string The slot constant (e.g., "HeadSlot", "ChestSlot")
--- @return table<number, boolean> tokens Table of item IDs for the given slot (keys are item IDs, values are true)
function TokenData:GetTokensForSlot(slot)
	local tokens = {}
	for itemID, itemSlot in pairs(LoothingTokenTable) do
		if itemSlot == slot then
			tokens[itemID] = true
		end
	end
	return tokens
end
